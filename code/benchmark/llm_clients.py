"""
llm_clients.py — Unified wrapper for LLM API calls.

Provides a single interface `call_llm(model_id, prompt, **kwargs)` that dispatches
to the appropriate provider SDK. Handles retries, timeouts, quality gates, and
metadata capture.

REAL execution requires:
    - OPENAI_API_KEY, ANTHROPIC_API_KEY, GOOGLE_API_KEY, MARITACA_API_KEY,
      ALIBABA_API_KEY (for Qwen), TOGETHER_API_KEY or FIREWORKS_API_KEY (for Llama 4)
    - pip install openai anthropic google-generativeai maritaca-api

For pipeline validation (current stage), a SYNTHETIC_MODE flag produces
deterministic plausible-looking responses without API calls. This lets us
validate the analysis pipeline end-to-end before committing API budget.
"""

from __future__ import annotations
import hashlib
import json
import os
