from .config import EXPERIMENT, LLMS, LLM


SYNTHETIC_MODE = os.environ.get("BENCHMARK_SYNTHETIC", "1") == "1"


@dataclass
class LLMResponse:
    model_id: str
    model_version: str
    prompt: str
    prompt_hash: str           # SHA-256 of prompt, for linking
    language: str              # ISO 639-1
    response_text: str
    response_tokens: int
    prompt_tokens: int
    finish_reason: str
    latency_ms: int
    timestamp_utc: str
    api_error: str | None = None
    retry_count: int = 0
    replicate_idx: int = 0

    def to_jsonl_record(self) -> str:
        return json.dumps(asdict(self), ensure_ascii=False)


def _hash_prompt(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()[:16]


# =========================================================================
# SYNTHETIC MODE — for pipeline validation without API costs
# =========================================================================

def _synthetic_response(model: LLM, prompt: str, language: str,
                        replicate_idx: int) -> LLMResponse:
    """Generate deterministic plausible synthetic response.

    Used to validate the full analysis pipeline before committing API budget.
    Explicitly marked in the response text so it can never be confused with
    real data.
    """
    import numpy as np

    # Seed from prompt + model + replicate for reproducibility
    seed_str = f"{model.id}|{_hash_prompt(prompt)}|{replicate_idx}"
    rng = np.random.default_rng(
        int(hashlib.sha256(seed_str.encode()).hexdigest()[:8], 16)
    )

    # Simulated latency and tokens
    latency = int(rng.gamma(shape=2.0, scale=800))     # ~600-3000ms realistic
    resp_tokens = int(rng.gamma(shape=2.0, scale=100))  # 100-400 tokens typical

    synthetic_text = (
        "[SYNTHETIC] Pipeline validation stub. Response would be generated by "
        f"{model.api_model_string} for prompt ({language}): "
        f"{prompt[:120]}..."
    )

    return LLMResponse(
        model_id=model.id,
        model_version=model.api_model_string,
        prompt=prompt,
        prompt_hash=_hash_prompt(prompt),
        language=language,
        response_text=synthetic_text,
        response_tokens=resp_tokens,
        prompt_tokens=len(prompt) // 4,
        finish_reason="stop",
        latency_ms=latency,
        timestamp_utc=datetime.now(timezone.utc).isoformat(),
        retry_count=0,
        replicate_idx=replicate_idx,
    )


